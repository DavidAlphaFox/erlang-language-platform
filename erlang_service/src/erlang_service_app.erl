%%==============================================================================
%% Application Callback Module
%%==============================================================================
-module(erlang_service_app).

%%==============================================================================
%% Behaviours
%%==============================================================================
-behaviour(application).

%%==============================================================================
%% Exports
%%==============================================================================
%% Application Callbacks
-export([
    start/2,
    stop/1
]).

%%==============================================================================
%% Application Callbacks
%%==============================================================================
-spec start(normal, noargs) -> {ok, pid()}.
start(_StartType, noargs) ->
    erlang_service_sup:start_link().

-spec stop(any()) -> ok.
stop(_State) ->
    ok.
